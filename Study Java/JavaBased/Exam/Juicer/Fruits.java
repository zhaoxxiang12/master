package Exam.Juicer;

public class Fruits {
    private String FruitName;

    public Fruits() {
        super();
    }

    public Fruits(String FruitName) {
        super();
        this.FruitName = FruitName;
    }

    public String getFruitName() {
        return FruitName;
    }

    public void setFruitName(String fruitName) {
        FruitName = fruitName;
    }
}
