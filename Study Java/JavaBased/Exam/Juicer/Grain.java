package Exam.Juicer;

public class Grain {
    private String GrainName;

    public Grain() {
        super();
    }

    public String getGrainName() {
        return GrainName;
    }

    public void setGrainName(String grainName) {
        GrainName = grainName;
    }

    public Grain(String GrainName) {
        super();
        this.GrainName = GrainName;
    }
}
