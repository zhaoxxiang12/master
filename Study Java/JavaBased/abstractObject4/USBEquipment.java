package abstractObject4;

public class USBEquipment {
    private String name;

    public USBEquipment() {
        super();
    }

    public USBEquipment(String name) {
        super();
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
